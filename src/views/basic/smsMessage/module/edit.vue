<template>
  <div>
    <el-button :disabled="data.id === 1" size="mini" type="success" @click="to">编辑</el-button>
    <eForm ref="form" :sup_this="sup_this" :is-add="false"/>
  </div>
</template>
<script>
import eForm from './form'
export default {
  components: { eForm },
  props: {
    data: {
      type: Object,
      required: true
    },
    sup_this: {
      type: Object,
      required: true
    }
  },
  methods: {
    to() {
      const _this = this.$refs.form
      _this.form = {id:this.data.id,phone: this.data.phone,custId: this.data.custId,content: this.data.content,smsType: this.data.smsType,bizId: this.data.bizId,status: this.data.status,sendTime: this.data.sendTime,errorMsg: this.data.errorMsg }
      _this.dialog = true
    }
  }
}
</script>

