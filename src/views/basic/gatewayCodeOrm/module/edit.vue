<template>
  <div>
    <el-button :disabled="data.id === 1" size="mini" type="success" @click="to">编辑</el-button>
    <eForm ref="form" :sup_this="sup_this" :is-add="false"/>
  </div>
</template>
<script>
import eForm from './form'
export default {
  components: { eForm },
  props: {
    data: {
      type: Object,
      required: true
    },
    sup_this: {
      type: Object,
      required: true
    }
  },
  methods: {
    to() {
      const _this = this.$refs.form
      _this.form = {id:this.data.id,codeId: this.data.codeId,channelId: this.data.channelId,channelCode: this.data.channelCode,channelMsg: this.data.channelMsg,normCode: this.data.normCode,normMsg: this.data.normMsg }
      _this.dialog = true
    }
  }
}
</script>

